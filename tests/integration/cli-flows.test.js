test('multi-target sync updates installed targets in stable order', async () => {
  const repoRoot = await createTempRepo();
  await execCli(repoRoot, ['init']);
  await execCli(repoRoot, ['install', '--agent', 'codex']);
  await execCli(repoRoot, ['install', '--agent', 'claude']);
  await execCli(repoRoot, ['install', '--agent', 'copilot']);
  await execCli(repoRoot, ['install', '--agent', 'opencode']);

  await writeCanonical(repoRoot, '.prodify/AGENTS.md', '# Shared Canonical\n');
  await writeCanonical(repoRoot, '.prodify/project.md', '# Project\n\nShared project context.\n');

  const result = await execCli(repoRoot, ['sync']);
  const lines = result.stdout.trim().split('\n');

  assert.equal(result.exitCode, 0);
  assert.deepEqual(lines, [
    `sync claude: updated ${path.join(repoRoot, 'CLAUDE.md')}`,
    `sync codex: updated ${path.join(repoRoot, 'AGENTS.md')}`,
    `sync copilot: updated ${path.join(repoRoot, '.github', 'copilot-instructions.md')}`,
    `sync opencode: updated ${path.join(repoRoot, '.opencode', 'AGENTS.md')}`
  ]);
});

